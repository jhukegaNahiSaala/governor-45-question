let places_I_Love = ['Paris', 'Gilgit' , 'Naran' , 'Kaghan' , 'Muree']
console.log(places_I_Love);


// print in alphabetical order

let Alphabetical_order = places_I_Love.slice(0 , places_I_Love.length)
console.log(Alphabetical_order.sort());
console.log(places_I_Love);

// reverse alphabetical order

console.log(Alphabetical_order.sort().reverse());
console.log(places_I_Love);

// reverse original array

places_I_Love.reverse()
console.log(places_I_Love)

// reverse again original array
places_I_Love.reverse()
console.log(places_I_Love)

// alphabetical order
places_I_Love.sort()
console.log(places_I_Love);

// reverse alphabetical order

places_I_Love.reverse()
console.log(places_I_Love);

